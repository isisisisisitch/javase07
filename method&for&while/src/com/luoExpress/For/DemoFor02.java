package com.luoExpress.For;

public class DemoFor02 {
    public static void main(String[] args) {
        int size = 20;
        for (int i = 1; i <=size ; i++) {
            if (i <=size-1) {
                System.out.print(i + ",");
            }else {
                System.out.print(i);
            }

        }
    }
}
